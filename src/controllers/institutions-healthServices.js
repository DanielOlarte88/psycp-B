const { institutions_healthServicesModel } = require("../database/models");
const { handleHttpError } = require("../database/utils/handleError");

const getItems = async (req, res) => {
  try {
    const person = req.person;
    const data = await institutions_healthServicesModel.findAll({});
    res.send({ data, person });
  } catch (e) {
    console.log(e);
    handleHttpError(res, "ERROR_GET_ITEMS");
  }
};

const getItem = async (req, res) => {
  try {
    const { id } = req.params;
    const institutions_healthServices_id = id;
    const data = await institutions_healthServicesModel.findOne({
      where: {
        institutions_healthServices_id,
      },
    });
    res.send({ data });
  } catch (e) {
    handleHttpError(res, "ERROR_GET_ITEM");
  }
};

const createItem = async (req, res) => {
  try {
    const body = req.body;
    const data = await institutions_healthServicesModel.create(body);
    res.status(201);
    res.send({ data });
  } catch (e) {
    handleHttpError(res, "ERROR_CREATE_ITEMS");
  }
};

const updateItem = async (req, res) => {
  try {
    const { id } = req.params;
    const institutions_healthServices_id = id;
    const { 
      institutions_institutions_id,
      health_services_health_services_id,
      activate } = req.body;
    const data = await institutions_healthServicesModel.findByPk(institutions_healthServices_id);
    data.institutions_institutions_id = institutions_institutions_id;
    data.health_services_health_services_id = health_services_health_services_id;
    data.activate = activate;
    await data.save();
    res.status(200);
    res.send({ data });
  } catch (e) {
    handleHttpError(res, "ERROR_UPDATE_ITEMS");
  }
};

const deleteItem = async (req, res) => {
  try {
    const { id } = req.params;
    const institutions_healthServices_id = id;
    const data = await institutions_healthServicesModel.destroy({
      where: {
        institutions_healthServices_id,
      },
    });
    res.status(204);
    res.send({ data });
  } catch (e) {
    console.log(e);
    handleHttpError(res, "ERROR_DELETE_ITEM");
  }
};

module.exports = { getItems, getItem, createItem, updateItem, deleteItem };
